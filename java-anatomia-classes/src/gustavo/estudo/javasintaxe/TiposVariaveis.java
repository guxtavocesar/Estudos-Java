package gustavo.estudo.javasintaxe;

public class TiposVariaveis {
	public static void main(String[] args) {
		
		/*Java variables declaration examples*/
		String nome = "Gustavo César";
		int idAluno = 19;
		float mediaAluno = 9.5F;
		double salarioMinimo = 2500.55;
		
		System.out.println("O nome do aluno é " + nome + ", seu ID é " + idAluno + ", sua média é " + mediaAluno + " e seu salário é " + salarioMinimo);
		
		/* Java constants declaration examples*/
		final double VALOR_DE_PI = 3.14;
		
		System.out.println("O valor de PI é " + VALOR_DE_PI);
	}
}
